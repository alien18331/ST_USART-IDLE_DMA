void USER_UART_IRQHandler(UART_HandleTypeDef *huart)
{
	if(huart->Instance == USART2)                                   //判断是否是串口1（！此处应写(huart->Instance == USART1)
    {
        if(RESET != __HAL_UART_GET_FLAG(&huart2, UART_FLAG_IDLE))   //判断是否是空闲中断
        {
            __HAL_UART_CLEAR_IDLEFLAG(&huart2);                     //清楚空闲中断标志（否则会一直不断进入中断）
            printf("\r\nUART2 Idle IQR Detected\r\n");
            USAR_UART_IDLECallback(huart);                          //调用中断处理函数
        }
    }
}

void USAR_UART_IDLECallback(UART_HandleTypeDef *huart)
{
	//STOP DMA
    HAL_UART_DMAStop(&huart2);

    uint8_t data_length  = BUFFER_SIZE - __HAL_DMA_GET_COUNTER(&hdma_usart2_rx);   //计算接收到的数据长度

    printf("Receive Data(length = %d): ",data_length);
    HAL_UART_Transmit(&huart2,receive_buff,data_length,0x200);                     //测试函数：将接收到的数据打印出去
    printf("\r\n");

    //reset buffer
    memset(receive_buff,0,data_length);
    data_length = 0;

    //restart DMA and transmit 255 byte per times.
    HAL_UART_Receive_DMA(&huart2, (uint8_t*)receive_buff, 255);
}
